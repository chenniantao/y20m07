const EventEmitter = require('events')
class CustomEventEmitter extends EventEmitter {

}
const emitter = new CustomEventEmitter()

//设置最大监听数
emitter.setMaxListeners(11) 

for(let i  = 1;i<=11;i++){
    emitter.on('test', () => {
        console.log(`fn${i}...`);
    })
}
emitter.emit('test')
